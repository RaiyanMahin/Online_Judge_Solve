#include <bits/stdc++.h>
#define ll long long
 
using namespace std;
 
 
void baal()
{
    ll a,  b; cin >> a >> b;
    if(a * 2 > b)
        cout << "YES\n";
    if(a * 2 <= b)
        cout << "NO\n";
 
    return;
 
 
}
 
int main()
{
 
    ll t; cin >> t;
    while(t--)
    {
        baal();
 
 
    }
 
}