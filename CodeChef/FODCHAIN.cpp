#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
#define debug(x) cerr << # x << ' ' << x << endl;

void baal()
{
   ll n , m; cin >> n >> m;
   if(n < m)
   {
       cout << "1\n"; return;
   }
   else
   {
       ll c = 0;
       while(n)
       {
           n /= m; c++;
       }
       cout << c << endl; return;
   }







    return;









}

int main()
{
    ll t; cin >> t;

    while(t--)

    {

        baal();
    }




}



